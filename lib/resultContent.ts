export function buildMirrorLine(
  answers: { q3?: string[]; q4?: string[]; q5?: string },
  primaryCause: Cause
): string {
  const mentions: string[] = [];

  for (const opt of answers.q3 ?? []) {
    if (opt === "გაყოფის ხაზი გაფართოვდა") mentions.push("გაყოფის ხაზის გაფართოება");
    else if (opt === "ცხიმიანი სკალპი") mentions.push("ცხიმიანი სკალპი");
    else if (opt === "მშრალი სკალპი") mentions.push("მშრალი სკალპი");
    else if (opt === "მეტი თმა რჩება სავარცხელზე") mentions.push("გაზრდილი ცვენა");
    else if (opt === "თხემზე სკალპი მოჩანს") mentions.push("სკალპის გამჭვირვალობა");
    else if (opt === "თმა ტყდება და დაკარგა ბზინვარება") mentions.push("თმის სისუსტე");
  }

  for (const opt of answers.q4 ?? []) {
    if (opt === "ციკლი არარეგულარული გახდა ან შეწყდა") mentions.push("ციკლის ცვლილება");
    else if (opt === "სიცხის შემოტევები ან ღამის ოფლიანობა") mentions.push("სიცხის შემოტევები");
    else if (opt === "მეტი სტრესი ან შფოთვა") mentions.push("გაზრდილი სტრესი");
    else if (opt === "წონის ცვლილება") mentions.push("წონის ცვლილება");
  }

  if (answers.q5 && answers.q5 !== "კარგად, ვისვენებ") mentions.push("ძილის პრობლემები");

  const top = mentions.slice(0, 3);

  const link: Record<Cause, string> = {
    hormonal: "ეს ყველაფერი ჰორმონულ ცვლილებაზე მიუთითებს",
    follicle: "ეს ყველაფერი ფოლიკულის მგრძნობელობაზე მიუთითებს",
    stress:   "ეს ყველაფერი სტრესსა და ძილის ხარისზე მიუთითებს",
    nutrient: "ეს ყველაფერი საკვები ნივთიერებების დეფიციტზე მიუთითებს",
    aging:    "ეს ყველაფერი სკალპის გარემოს ცვლილებაზე მიუთითებს",
  };

  if (top.length === 0) return `${link[primaryCause]}.`;
  return `შენ აღნიშნე ${top.join(", ")} — ${link[primaryCause]}.`;
}
